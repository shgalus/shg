static_assert(std::size(entry_type) == nenttypes);

std::vector<Category> generate_all_categories() {
     std::vector<Category> v;
     Category c;

     for (auto const& et : entry_type) {
          unsigned short const n =
               number_of_forms(et.category.inflexion);
          for (unsigned short form = 0; form < n; form++) {
               c = et.category;
               complete_category(c, c.inflexion, form);
               v.push_back(c);
          }
     }
     return v;
}

